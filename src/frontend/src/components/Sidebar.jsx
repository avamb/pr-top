/* ── Helper: is active? ───────────────────────────────── */
function isActive(itemPath, currentPath) {
  if (itemPath === '/dashboard' && currentPath === '/dashboard') return true;
  if (itemPath === '/admin' && currentPath === '/admin') return true;
  // For other items, match prefix but not the root paths
  if (itemPath !== '/dashboard' && itemPath !== '/admin') {
    return currentPath.startsWith(itemPath);
  }
  return false;
}

/* ═══════════════════════════════════════════════════════ */
/*                     Sidebar Component                   */
/* ═══════════════════════════════════════════════════════ */
export default function Sidebar({ user }) {
  const navigate = useNavigate();
  const location = useLocation();
  const { t } = useTranslation();

  const handleLogout = () => {
    localStorage.removeItem('token');
    localStorage.removeItem('user');
    navigate('/');
  };

  const isSuperadmin = user?.role === 'superadmin';

  // Build nav items
  const mainItems = therapistNavItems;
  const extraItems = isSuperadmin ? adminNavItems : [];

  return (
    <aside
      aria-label="Sidebar navigation"
      className="fixed top-0 left-0 h-full w-60 bg-white border-r border-gray-200 flex flex-col z-40"
    >
      {/* Brand */}
      <div className="px-5 py-5 border-b border-gray-100 flex items-center gap-2">
        <span className="text-xl font-bold text-primary tracking-tight">{t('brand')}</span>
        {isSuperadmin && (
          <span className="bg-red-100 text-red-700 text-[10px] font-semibold px-1.5 py-0.5 rounded-full uppercase">
            Admin
          </span>
        )}
      </div>

      {/* Main nav */}
      <nav className="flex-1 overflow-y-auto py-4 px-3">
        <ul className="space-y-1">
          {mainItems.map(item => {
            const active = isActive(item.path, location.pathname);
            const Icon = item.icon;
            return (
              <li key={item.key}>
                <button
                  onClick={() => navigate(item.path)}
                  className={`w-full flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                    active
                      ? 'bg-primary/10 text-primary'
                      : 'text-stone-600 hover:bg-stone-50 hover:text-stone-900'
                  }`}
                  aria-current={active ? 'page' : undefined}
                >
                  <Icon />
                  {t(item.labelKey)}
                </button>
              </li>
            );
          })}
        </ul>

        {/* Admin section */}
        {extraItems.length > 0 && (
          <>
            <div className="my-4 border-t border-gray-100" />
            <p className="px-3 mb-2 text-[11px] font-semibold uppercase tracking-wider text-stone-400">
              {t('nav.adminSection')}
            </p>
            <ul className="space-y-1">
              {extraItems.map(item => {
                const active = isActive(item.path, location.pathname);
                const Icon = item.icon;
                return (
                  <li key={item.key}>
                    <button
                      onClick={() => navigate(item.path)}
                      className={`w-full flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                        active
                          ? 'bg-red-50 text-red-700'
                          : 'text-stone-600 hover:bg-stone-50 hover:text-stone-900'
                      }`}
                      aria-current={active ? 'page' : undefined}
                    >
                      <Icon />
                      {t(item.labelKey)}
                    </button>
                  </li>
                );
              })}
            </ul>
          </>
        )}
      </nav>

      {/* User footer */}
      <div className="border-t border-gray-100 px-3 py-4 space-y-2">
        <div className="px-3 text-xs text-stone-500 truncate">{user?.email}</div>
        <button
          onClick={handleLogout}
          className="w-full flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium text-stone-600 hover:bg-red-50 hover:text-red-700 transition-colors"
        >
          <LogoutIcon />
          {t('nav.logout')}
        </button>
      </div>
    </aside>
  );
}
